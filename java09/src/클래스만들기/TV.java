package 클래스만들기;
//class (부류, 같은 성격을 가진 group)
//tv부류가 일반적으로 
public class TV {

	// 함수정의=>메서드(method, 방법을 정의)
	// 키다
	public void on() {
		System.out.println("켜다");
		System.out.println("코드를 끼다");
	}

	// 끄다
	public void off() {
		System.out.println("끄다");
		System.out.println("코드를 뽑다");
	}
}
